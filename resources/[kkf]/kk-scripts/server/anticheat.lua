local function log(source, description)
    TriggerClientEvent('KKF.AntiCheat.Screenshot', source, description)
end

RegisterServerEvent('KKF.AntiCheat.SendLog', function(data)
    local xPlayer = ESX.GetPlayerFromId(source)
    local message = 'PID: ' .. xPlayer.identifier .. '; NIMI: ' .. xPlayer.name .. '; SISU: ' .. data[2]

    PerformHttpRequest(webHook, function(err, text, headers) end, 'POST', json.encode({ username = 'AC Logs', content = message .. ' ' .. data[1]}), { ['Content-Type'] = 'application/json' })
end)

for i = 1, #Blacklist.Events do
    RegisterServerEvent(Blacklist.Events[i], function()
        local src = source

        log(src, 'Keelatud eventi ' .. Blacklist.Events[i] .. ' kasutamine.')
        TriggerEvent('kk-admin:banCheat', src, 'Keelatud eventi ' .. Blacklist.Events[i] .. ' kasutamine.')
    end)
end

AddEventHandler('entityCreating', function(entity)
    local src = NetworkGetEntityOwner(entity)
    local model = GetEntityModel(entity)

    for i=1 , #Blacklist.Entities do
        if model == GetHashKey(Blacklist.Entities[i]) then
            CancelEvent()
            TriggerClientEvent('KKF.AntiCheat.DeleteEntity', -1, entity)
            log(src, 'Keelatud entity spawnimine.')
            break
        end
    end
end)

AddEventHandler('explosionEvent', function(sender, ev)
    local src = sender

    log(src, 'Explosioni tekitamine.')
end)